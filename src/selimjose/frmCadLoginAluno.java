    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        // Criando o Login de Aluno
        final clnAluno u = new clnAluno();
        clnAluno a = new clnAluno();
        String senha = (txtPassword.getText());
        String login = txtUsuario.getText();
        if (login.equals("") || senha.equals("")) {
            if (login.equals("")) {
                JOptionPane.showMessageDialog(this, "Coloque o login do Usuario", "Login", JOptionPane.WARNING_MESSAGE);
                txtUsuario.requestFocus();
            }
            if (senha.equals("")) {
                JOptionPane.showMessageDialog(this, "Coloque a senha do Usuario", "Login", JOptionPane.WARNING_MESSAGE);
                txtPassword.requestFocus();
            }
        } else {            
            AlunoDao alunoDAO = new AlunoDao();
            
            u.setLogin(login);
            u.setSenha(senha);            
            try {
                a = alunoDAO.Logar(u);
            } catch (DaoException ex) {
                Logger.getLogger(frmCadLoginAluno.class.getName()).log(Level.SEVERE, null, ex);
            }
            u.setCdUsuario(a.getCdUsuario());
            System.out.println(u.getCdUsuario());
            try {
                if (alunoDAO.Logar(u) != null) {
                    
                    java.awt.EventQueue.invokeLater(new Runnable() {
                        public void run() {
                            frmMovimentacaoFinal m;
                            try {
                                m = new frmMovimentacaoFinal(u,i);
                                m.setLocationRelativeTo(null);
                                m.setResizable(true);
                                m.setVisible(true);
                            } catch (DaoException ex) {
                                Logger.getLogger(frmCadLoginAluno.class.getName()).log(Level.SEVERE, null, ex);
                            }
                                                       
                        }
                    });
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Senha ou Usuario não existentes", "Login", JOptionPane.ERROR_MESSAGE);
                }
            } catch (DaoException ex) {
                Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

